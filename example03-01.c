#include<stdio.h>

int main(void)
{
	printf("%c\n", 'K');
	printf("%c\n", 65);
	printf("%d\n", 12);
	printf("%i\n", 12);
	printf("%o\n", 12);
	printf("%u\n", 12);
	printf("%x\n", 27);
	printf("%X\n", 27);
	printf("%f\n", 12.34);
	printf("%5.2f\n", 12.34);
	printf("%e\n", 123.45);
	printf("%E\n", 123.45);
	printf("%s\n", "kim");
	printf("%p\n", "kim");

	return 0;
}
